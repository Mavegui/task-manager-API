@pytest.mark.django_db
def test_update_task_fields(api_client_auth, user_alex):
    """Ensures that (PUT/PATCH) it's OK"""
    # Created innitial task
    task = Task.objects.create(
        title="Título Antigo",
        description="Descrição antiga com mais de vinte caracteres aqui.",
        priority="L",
        user=user_alex,
    )
    
    url = reverse("task-detail", args=[task.id])
    payload = {"title": "Título Novo", "completed": True}

    # PATCH 
    response = api_client_auth.patch(url, payload, format="json")

    assert response.status_code == status.HTTP_200_OK
    assert response.data["title"] == "Título Novo"
    assert response.data["completed"] is True

@pytest.mark.django_db
def test_alex_cannot_modify_or_delete_beatriz_task(api_client_auth, user_beatriz):
    """Security test"""
    task_beatriz = Task.objects.create(
        title="Secreta da Beatriz", 
        description="Descrição longa da tarefa da Beatriz aqui.", 
        priority="M", 
        user=user_beatriz
    )
    url = reverse("task-detail", args=[task_beatriz.id])
    
    # Alex PATCH in Beatriz task
    response_patch = api_client_auth.patch(url, {"title": "Invasão"}, format="json")
    assert response_patch.status_code == status.HTTP_404_NOT_FOUND

    # Alex DELETE in Beatriz task
    response_delete = api_client_auth.delete(url)
    assert response_delete.status_code == status.HTTP_404_NOT_FOUND

