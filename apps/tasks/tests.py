@pytest.mark.django_db
def test_get_queryset_privacy_with_pytest(api_client_auth, user_alex, user_beatriz):
    """Test if Alex can't see data generated by Beatriz"""
    url = reverse("task-list")

    #  Created 1 task for Alex using the Faker
    Task.objects.create(
        title=fake.catch_phrase(),
        description=fake.text(max_nb_chars=50),
        priority="L",
        user=user_alex,
    )

    #  Created 3 random tasks for Beatriz
    for _ in range(3):
        Task.objects.create(
            title=fake.catch_phrase(),
            description=fake.text(max_nb_chars=50),
            priority="H",
            user=user_beatriz,
        )
        
    response = api_client_auth.get(url)
    assert response.status_code == status.HTTP_200_OK
    results = response.data.get("results", response.data)
    # Alex should only be able to see 1 task in the return of his listing
    assert len(results) == 1
        
