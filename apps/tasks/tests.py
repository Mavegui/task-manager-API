@pytest.mark.django_db
def test_create_task_with_faker(api_client_auth, user_primary):
    """Ensure the creation of a task using Faker"""
    url = reverse("task-list")
    
    # Faker generates random data
    payload = {
        "title": fake.catch_phrase(),
        "description": fake.text(max_nb_chars=100),
        "priority": "M",
    }

    response = api_client_auth.post(url, payload, format="json")

    assert response.status_code == status.HTTP_201_CREATED
    assert response.data["title"] == payload["title"]
    assert Task.objects.latest("id").user == user_primary


