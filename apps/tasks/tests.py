import pytest
from django.contrib.auth.models import User
from django.urls import reverse
from faker import Faker
from rest_framework import status
from rest_framework.test import APIClient
from rest_framework_simplejwt.tokens import RefreshToken

from apps.tasks.models import Task

fake = Faker("pt_BR")

@pytest.fixture
def api_client():
    """Generates the client tests of Django Rest Framework"""
    return APIClient()

@pytest.fixture
def user_alex(db):
    """Created user Alex in the  test db"""
    return User.objects.create_user(username="alex", password="senha_segura123")


@pytest.fixture
def user_beatriz(db):
    """Created user Beatriz in the test db"""
    return User.objects.create_user(username="beatriz", password="senha_segura456")


@pytest.fixture
def token_alex(user_alex):
    """Token generated JWT for Alex automatic"""
    refresh = RefreshToken.for_user(user_alex)
    return str(refresh.access_token)


@pytest.fixture
def api_client_auth(api_client, token_alex):
    """Return the already authenticated HTTP client with the Alex Token"""
    api_client.credentials(HTTP_AUTHORIZATION=f"Bearer {token_alex}")
    return api_client


@pytest.mark.django_db
def test_create_task_with_faker(api_client_auth, user_alex):
    """Ensure the creation of a task using Faker"""
    url = reverse("task-list")
    
    # Faker generates random data
    payload = {
        "title": fake.catch_phrase(),
        "description": fake.text(max_nb_chars=100),
        "priority": "M",
    }

    response = api_client_auth.post(url, payload, format="json")

    assert response.status_code == status.HTTP_201_CREATED
    assert response.data["title"] == payload["title"]
    assert Task.objects.latest("id").user == user_alex


