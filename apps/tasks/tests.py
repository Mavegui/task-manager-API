@pytest.mark.django_db
def test_get_queryset_privacy_with_pytest(api_client_auth, user_primary, user_secondary):
    """Test if primary user can't see data generated by secondary user"""
    url = reverse("task-list")

    # Created 1 task for primary user using the Faker
    Task.objects.create(
        title=fake.catch_phrase(),
        description=fake.text(max_nb_chars=50),
        priority="L",
        user=user_primary,
    )

    # Created 3 random tasks for secondary user
    for _ in range(3):
        Task.objects.create(
            title=fake.catch_phrase(),
            description=fake.text(max_nb_chars=50),
            priority="H",
            user=user_secondary,
        )
        
    response = api_client_auth.get(url)
    assert response.status_code == status.HTTP_200_OK
    results = response.data.get("results", response.data)
    # Primary user should only be able to see 1 task in the return of his listing
    assert len(results) == 1
        
